    public static List<Integer> freqQuery(int[][] queries) {
        int tempIndex;
        Map<Integer, Integer> list = new HashMap<>();
        Map<Integer, Integer> frequency = new HashMap<>();
        List<Integer> result = new ArrayList<>();
        for (int[] query : queries) {
            if (query[0] == 1) {
                //Add element
                if (list.containsKey(query[1])) {
                    tempIndex = list.get(query[1]);
                    changeFrequency(frequency, tempIndex, tempIndex + 1);
                    list.replace(query[1], tempIndex + 1);
                } else {
                    changeFrequency(frequency, 0, 1);
                    list.put(query[1], 1);
                }
            } else if (query[0] == 2) {
                //delete element
                Integer value = list.get(query[1]);
                if (value != null) {
                    if (value == 1) {
                        list.remove(query[1]);
                        changeFrequency(frequency, 1, 0);
                    } else {
                        tempIndex = list.get(query[1]);
                        changeFrequency(frequency, tempIndex, tempIndex - 1);
                        list.replace(query[1], list.get(query[1]) - 1);
                    }
                }
            } else {
                Integer tempInt = frequency.get(query[1]);
                if (tempInt == null) {
                    tempInt = 0;
                }

                if (tempInt > 0) {
                    result.add(1);
                } else {
                    result.add(0);
                }
            }
        }
        return result;
    }

    private static void changeFrequency(Map<Integer, Integer> frequency, int oldFr, int newFr) {
        if (frequency.containsKey(oldFr)) {
            frequency.replace(oldFr, frequency.get(oldFr) - 1);
        }
        if (frequency.containsKey(newFr)) {
            frequency.replace(newFr, frequency.get(newFr) + 1);
        } else {
            frequency.put(newFr, 1);
        }
    }

